      </p>

      <p>
        Los meridianos son círculos máximos que pasan por los polos y los paralelos son círculos menores,
        paralelos al Ecuador que van estrechándose hasta convertirse en un punto en los polos.
      </p>

      <p>
        Pero es necesario precisar el concepto de "polo". Lo habitual es definir al "polo" como el punto donde
        el eje imaginario de rotación de la Tierra intersecta la superficie terrestre, y esta es una buena definición
        pero en este caso es necesario denominar a tal punto "polo de rotación".
      </p>
      <h3>3. El movimiento del polo y la rotación terrestre</h3>
      <p>
        Cuando se obtienen las coordenadas geográficas (latitud y longitud) a partir de las observaciones de estrellas,
        satélites o cualquier objeto exterior a la Tierra, estas coordenadas resultan naturalmente expresadas
        en un sistema de meridianos y paralelos cuyo punto de origen es el "polo de rotación".
      </p>

      <figure class="article-inline-image">
        <img src="@/assets/images/polo.jpg" alt="Movimiento del polo" />
        <figcaption>
En esta secuencia se ve la trayectoria del primer meteoro fotografiado con la cámara especial instalada en la EARG, durante la noche del 2 al 3 de agosto de 2011. Gentileza de Marilia Samara and Robert Mitchell, SouthWest Research Institute.
        </figcaption>
      </figure>
      <p>
        A fines del siglo pasado se detectaron variaciones en la latitud del Observatorio de Berlín.
        Esto condujo a la realización de una campaña de observación de la que participaron varios observatorios de la época,
        que rápidamente pusieron de manifiesto que las variaciones de latitud y longitud observadas
        se debían al cambio de posición del eje de rotación dentro de la Tierra misma ("movimiento del polo").
      </p>

      <p>
        Este movimiento del origen del sistema de referencia terrestre (polo de rotación)
        cambia de posición los paralelos y meridianos respecto de la superficie terrestre;
        en consecuencia, cambian las coordenadas "latitud y longitud".
      </p>

      <p>
        Este problema del movimiento del polo presenta un interés doble:
      </p>

      <ol>
        <li> encontrar los mecanismos geofísicos y astronómicos que lo generan.</li>
        <li> encontrar la manera de corregir las determinaciones de latitud y longitud
        para contar con un sistema de referencia terrestre que esté fijo a la Tierra.</li>
      </ol>

      <p>
        Este segundo punto es muy importante, porque de lo contrario las coordenadas de un determinado lugar
        varían continuamente. Si se conoce la posición del polo de rotación sobre la superficie,
        es posible aplicar correcciones a las coordenadas instantáneas para que la posición de un lugar
        no dependa del momento en que se hagan las observaciones.
      </p>

      <p>
        A lo largo del tiempo, muchos observatorios del mundo dedicaron sus esfuerzos a contribuir con sus resultados
        a la determinación del movimiento del polo. La precisión de las observaciones aumentó paulatinamente hasta nuestros días,
        en que puede encontrarse la posición del polo de rotación sobre la superficie terrestre con una precisión de unos pocos centímetros.
      </p>

      <p>
        La amplitud del movimiento es pequeña. Desde principios de siglo, el polo de rotación ha permanecido
        en todo momento dentro de un cuadrado de aproximadamente 20 metros de lado.
        Sin embargo, estos movimientos involucran la presencia de fenómenos geofísicos en el interior de la Tierra
        y en su atmósfera de magnitud muy importante.
      </p>

      <p>
        Algunas causas del movimiento del polo son claras, pero estas no explican la trayectoria observada por completo.
        Por esta razón, es imprescindible observar y continuar los trabajos de investigación en este campo
        que está muy lejos de estar resuelto.
      </p>

      <p>
        La Tierra, en consecuencia, gira en torno a su eje de rotación que no está fijo dentro de la Tierra misma,
        y más aún, la velocidad de rotación alrededor de este eje instantáneo también es variable.
      </p>

      <p>
        La amplitud de estas variaciones es de unos pocos milésimos de segundo
        en la duración de una revolución completa de la Tierra.
      </p>

      <p>
        Dicho de otro modo, un día no dura lo mismo en junio que en enero.
        Tampoco la duración promedio de un día en 1990 es la misma que en 1989 o la de cualquier otro año.
        En particular, se ha encontrado un frenado progresivo en la velocidad de rotación terrestre
        que es de unos 2 milésimos de segundo por siglo.
      </p>

      <p>
        En estos casos se verifica una situación semejante a la descripta para el movimiento del polo.
        Estas variaciones en la velocidad de rotación son causadas por fenómenos geofísicos de gran magnitud
        y constituyen un campo de investigación permanente.
      </p>

      <p>
        Aunque se sospechó de estas variaciones desde el siglo pasado,
        solo pudieron ponerse de manifiesto por medio de la colaboración de varios observatorios en la década del treinta.
        El instrumental astronómico evolucionó entonces hacia telescopios como el Astrolabio de Danjon
        que permiten determinar simultáneamente las variaciones en la latitud del lugar
        y la hora medida en términos del "reloj Tierra".
      </p>

