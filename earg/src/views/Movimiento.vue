      <h3>2. Los sistemas de referencia terrestres y celestes</h3>

      <p>
        Un sistema de referencia es un sistema de ejes coordenados respecto de los cuales es posible trazar una curva
        o simplemente dibujar puntos, o puede ser un cuadro con referencias horizontales y verticales
        (por ejemplo para jugar a la batalla naval) donde cada lugar del cuadro se identifica claramente
        con una referencia particular. Las paredes de un cuarto son un sistema de referencia dentro del cuarto,
        los paralelos y meridianos terrestres constituyen un sistema para el posicionamiento del hombre sobre
        la superficie terrestre, etc.
      </p>

      <p>
        Dos sistemas de referencia pueden vincularse si se conocen las posiciones de objetos comunes.
      </p>

      <p>
        Por ejemplo, si se conoce la posición de una mesa dentro de una casa, y también se conoce la posición
        de la casa en un plano de la ciudad, puede determinarse con precisión la posición de la mesa en el plano de la ciudad.
      </p>

      <p>
        La importancia de los sistemas de referencia es enorme, aunque a menudo pasan inadvertidos por su utilización cotidiana.
        Son sistemas que ayudan permanentemente a los hombres a moverse en su casa, en la ciudad y sobre el globo terráqueo
        o en sus viajes espaciales.
      </p>

      <p>
        Precisamente las coordenadas de las estrellas materializan un sistema de referencia exterior a la Tierra.
        En primera aproximación podemos considerar que este sistema celeste está fijo en el espacio.
        Entiéndase bien, el sistema en su conjunto está fijo, en cambio los elementos que lo constituyen, las estrellas,
        tienen movimientos propios que es necesario determinar y perfeccionar para que todo el sistema no se degrade con el tiempo.
      </p>

      <p>
        En definitiva, para el mejoramiento y mantenimiento del sistema de referencia espacial constituido por las estrellas,
        es necesario observarlas a lo largo del tiempo para mejorar sus posiciones y encontrar sus movimientos.
      </p>

      <p>
        Por otra parte, resulta para todos más evidente la necesidad de contar con un sistema de referencia fijo a la Tierra.
        Tal sistema de referencia es el que permite a los hombres navegar, volar, construir caminos, y explorar recursos naturales,
        organizar y estudiar el uso de la tierra, fijar límites, dibujar mapas, etc.
      </p>

      <p>
        El más conocido de estos sistemas terrestres es el que permite posicionar un punto en base al sistema de meridianos y paralelos.
      </p>

      <p>
